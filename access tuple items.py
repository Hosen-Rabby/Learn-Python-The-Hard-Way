print("Can Access Tuple Items-by referring to the index number, inside square brackets")
thistuple = ("kat", "Gari", "toy","Laptop","TV")
print(thistuple[2])

print("Negative Indexing")
print("refers to the last item -",thistuple[-1])
print("refers to the 2nd last item -",thistuple[-2])

print("Range of Indexes")
print(thistuple[1:])
print(thistuple[1:6])
print(thistuple[:5])
print(thistuple[1:-1])